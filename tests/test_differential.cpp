        Context in;
        in.gpr[0] = rng();                  // dividend low (rax)
        in.gpr[2] = 0;                      // rdx = 0 (high half) -> no overflow
        in.gpr[1] = rng() | 1ULL;           // rcx divisor, guaranteed non-zero
